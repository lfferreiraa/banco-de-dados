CREATE TABLE audit.audit_log (
    audit_id       BIGINT GENERATED BY DEFAULT AS IDENTITY,
    changed_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    schema_name    TEXT NOT NULL,
    table_name     TEXT NOT NULL,
    operation      CHAR(1) NOT NULL,
    record_pk      TEXT,
    old_data       JSONB,
    new_data       JSONB,
    changed_by     TEXT NOT NULL DEFAULT current_user,
    request_id     UUID DEFAULT gen_random_uuid(),
    CONSTRAINT pk_audit_log PRIMARY KEY (audit_id, changed_at),
    CONSTRAINT ck_audit_operation CHECK (operation IN ('I', 'U', 'D'))
) PARTITION BY RANGE (changed_at);

CREATE TABLE audit.audit_log_2024 PARTITION OF audit.audit_log
    FOR VALUES FROM ('2024-01-01') TO ('2025-01-01');

CREATE TABLE audit.audit_log_2025 PARTITION OF audit.audit_log
    FOR VALUES FROM ('2025-01-01') TO ('2026-01-01');

CREATE TABLE audit.audit_log_2026 PARTITION OF audit.audit_log
    FOR VALUES FROM ('2026-01-01') TO ('2027-01-01');

CREATE TABLE audit.audit_log_future PARTITION OF audit.audit_log
    FOR VALUES FROM ('2027-01-01') TO ('2035-01-01');

CREATE TABLE audit.audit_log_default PARTITION OF audit.audit_log DEFAULT;

