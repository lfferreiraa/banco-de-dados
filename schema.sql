CREATE TABLE dw.fact_order_items (
    fact_order_item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id           UUID NOT NULL,
    date_key            INTEGER NOT NULL REFERENCES dw.dim_date (date_key),
    customer_key        BIGINT NOT NULL REFERENCES dw.dim_customer (customer_key),
    product_key         BIGINT NOT NULL REFERENCES dw.dim_product (product_key),
    seller_key          BIGINT NOT NULL REFERENCES dw.dim_seller (seller_key),
    order_id            BIGINT NOT NULL,
    order_item_id       BIGINT NOT NULL,
    quantity            INTEGER NOT NULL,
    gross_revenue       NUMERIC(14,2) NOT NULL,
    discount_amount     NUMERIC(14,2) NOT NULL,
    tax_amount          NUMERIC(14,2) NOT NULL,
    net_revenue         NUMERIC(14,2) NOT NULL,
    commission_amount   NUMERIC(14,2) NOT NULL,
    product_cost        NUMERIC(14,2) NOT NULL,
    contribution_margin NUMERIC(14,2) NOT NULL,
    loaded_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_fact_order_item UNIQUE (tenant_id, order_item_id),
    CONSTRAINT ck_fact_order_items_values
        CHECK (
            quantity > 0
            AND gross_revenue >= 0
            AND discount_amount >= 0
            AND tax_amount >= 0
            AND net_revenue >= 0
            AND commission_amount >= 0
            AND product_cost >= 0
        )
);

