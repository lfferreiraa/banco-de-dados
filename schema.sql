CREATE TABLE finance.seller_payouts (
    payout_id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id         UUID NOT NULL REFERENCES core.tenants (tenant_id),
    seller_id         BIGINT NOT NULL REFERENCES core.sellers (seller_id),
    period_start      DATE NOT NULL,
    period_end        DATE NOT NULL,
    gross_amount      NUMERIC(14,2) NOT NULL DEFAULT 0,
    commission_amount NUMERIC(14,2) NOT NULL DEFAULT 0,
    refund_amount     NUMERIC(14,2) NOT NULL DEFAULT 0,
    net_amount        NUMERIC(14,2) NOT NULL DEFAULT 0,
    payout_status     VARCHAR(30) NOT NULL DEFAULT 'calculated',
    paid_at           TIMESTAMPTZ,
    created_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_seller_payout_period UNIQUE (tenant_id, seller_id, period_start, period_end),
    CONSTRAINT ck_seller_payout_period CHECK (period_end >= period_start),
    CONSTRAINT ck_seller_payout_amounts
        CHECK (
            gross_amount >= 0
            AND commission_amount >= 0
            AND refund_amount >= 0
            AND net_amount >= 0
        ),
    CONSTRAINT ck_seller_payout_status
        CHECK (payout_status IN ('calculated', 'approved', 'paid', 'blocked'))
);

